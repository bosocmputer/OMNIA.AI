async function main() {
  for (const prompt of prompts) {
    const agent = await db.agent.findFirst({ where: { name: prompt.name } });
    if (!agent) {
      console.log(`skip: ${prompt.name}`);
      continue;
    }
    await db.agent.update({
      where: { id: agent.id },
      data: { role: prompt.role, soul: prompt.soul, updatedAt: new Date() },
    });
    console.log(`updated: ${prompt.name}`);
  }
}

main()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  })
  .finally(() => db.$disconnect());
